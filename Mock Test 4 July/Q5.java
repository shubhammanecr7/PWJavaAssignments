package Test;

public class Q5 {
	/*
	 * 5. Write a Java program to declare and initialize variables of different data
	 * types (int, double, String) with appropriate identifiers.
	 */
	public static void main(String[] args) {
		int myInteger = 10;
		double myDouble = 3.14;
		String myString = "Hello, World!";

		System.out.println("Integer value: " + myInteger);
		System.out.println("Double value: " + myDouble);
		System.out.println("String value: " + myString);
	}
}